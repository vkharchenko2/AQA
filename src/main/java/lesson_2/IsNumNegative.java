package lesson_2;

public class IsNumNegative {

    //Задание3. Написать метод, которому в качестве параметра передается целое число.
    // Метод должен вернуть true, если число отрицательное, и вернуть false если положительное.
    // Замечание: ноль считаем положительным числом.
    public static boolean isNumNegative(int number) {
        return number < 0;
    }
}
